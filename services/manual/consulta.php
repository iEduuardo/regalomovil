<h1>Consulta de una tarjeta</h1>
<table width="100%" border="0" cellspacing="0" cellpadding="0">
  <tr>
    <td  valign="top"><p>Ya que dentro de la tarjeta no aparece el monto de descuento, el cliente deberá acercarse a tí para consultarte dicho monto.</p>
    <p>El valor de la tarjeta puede ser de 200, 500, 1000 y 3500 pesos mexicanos, que serán descontados del monto de una compra.</p></td>
  </tr>
  <tr>
